package com.example.blooddonationbuddy.CreditsRecyclerView;

public class SourceItem {

    private int img;
    private int source;

    public SourceItem(int img, int source) {
        this.img = img;
        this.source = source;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public int getSource() {
        return source;
    }

    public void setSource(int source) {
        this.source = source;
    }
}
